 function getmonth(s){
    if(s=="Jan"){
        return "01";
    }
    else if(s=="Feb"){
        return "02";
    }
    else if(s=="Mar"){
        return "03";
    }
    else if(s=="Apr"){
        return "04";
    }
    else if(s=="May"){
        return "05";
    }
    else if(s=="Jun"){
        return "06";
    }
    else if(s=="Jul"){
        return "07";
    }
    else if(s=="Aug"){
        return "08";
    }
    else if(s=="Sep"){
        return "09";
    }
    else if(s=="Oct"){
        return "10";
    }
    else if(s=="Nov"){
        return "11";
    }
    else if(s=="Dec"){
        return "12";
    }
}

module.exports=
    getmonth    